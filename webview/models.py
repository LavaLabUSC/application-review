from django.db import models

class application(models.Model):
	fullName		 		= models.CharField(max_length=400)
	email			 		= models.CharField(max_length=400)
	uscId					= models.IntegerField(default=0)
	major			 		= models.CharField(max_length=400)
	minor			 		= models.CharField(max_length=400)
	gradYear				= models.IntegerField(default=0)
	available				= models.BooleanField(default=False)
	roles					= models.TextField()
	otherRole				= models.TextField()
	roleNotes				= models.TextField()
	desiredOutcome			= models.TextField()
	desiredOutcomeNotes		= models.TextField()
	contribution			= models.TextField()
	contributionNotes		= models.TextField()
	recentProj				= models.TextField()
	recentProjNotes			= models.TextField()
	dailyProb				= models.TextField()
	dailyProbNotes			= models.TextField()
	excitingTech			= models.TextField()
	excitingTechNotes		= models.TextField()
	devToolsSoft			= models.TextField()
	devToolsSoftNotes		= models.TextField()
	devToolsHard			= models.TextField()
	devToolsHardNotes		= models.TextField()
	designTools				= models.TextField()
	designToolsNotes		= models.TextField()
	otherTools				= models.TextField()
	otherToolsNotes			= models.TextField()
	resume					= models.URLField()
	resumeNotes				= models.TextField()
	portfolio				= models.URLField()
	portfolioNotes			= models.TextField()
	whatWork				= models.TextField()
	whatWorkNotes			= models.TextField()
	otherOrg				= models.TextField()
	otherOrgNotes			= models.TextField()
	referral				= models.TextField()
	joke					= models.TextField()
	annotated				= models.BooleanField(default=False)
	dedicationRating		= models.IntegerField()
	funRating				= models.IntegerField()
	experienceRating		= models.IntegerField()
	imaginationRating		= models.IntegerField()
	naughtyRating			= models.IntegerField()
	notes					= models.TextField()
	annotator				= models.CharField(max_length=100)
	final					= models.CharField(max_length=200)
